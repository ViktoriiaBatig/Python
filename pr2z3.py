def визначити_швидкість_вітру(швидкість):
  """
  Визначає швидкість вітру за заданим значенням.

  Args:
      швидкість: Швидкість вітру в м/с.

  Returns:
      Повідомлення про швидкість вітру.
  """
  if швидкість <= 4:
    return "Слабкий вітер."
  elif швидкість <= 10:
    return "Помірний вітер."
  elif швидкість <= 18:
    return "Сильний вітер."
  else:
    return "Ураганний вітер!"

# Приклад використання
швидкість_вітру = float(input("Введіть швидкість вітру (м/с): "))

повідомлення = визначити_швидкість_вітру(швидкість_вітру)

print(повідомлення)
